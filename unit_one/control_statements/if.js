/* Usando un if, crear una condición que compare si la variable numeroIf es positivo, negativo, o 0.
Pista: Los números inferiores a 0 son negativos y los superiores, positivos. */
/* Transcrito en ingles:
Using the "if" conditional statement, create a condition that compares if the value of the variable named "numeroIf" is positive, negative, or zero.
Clue: the numbers less than 0 are negative, and more than 0 are positive. */

let numeroIf = 5;
if (numeroIf == 0) {
  console.log("numeroIf es 0");
} else if (numeroIf > 0) {
  console.log("numeroIf es positivo");
} else {
  console.log("numeroIf es negativo");
}
