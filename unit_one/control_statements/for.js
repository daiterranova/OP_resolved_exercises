/*Para el bucle For, crea una variable numeroFor, esta variable tendrá como valor 0 y su condición será que la variable sea igual o menor que 3, se irá incrementando en 1 su valor cada vez que se ejecute y deberá mostrarse por pantalla.*/
/* Traduccion a ingles:
In the for loop, create a variable named "numeroFor" with the value of 0; its condition evaluates if this variable is equal or less than 3, if it is true, the value's variable increases one every time that it is executed and has to be printed on the screen. */

for (let numeroFor = 0; numeroFor <= 3; numeroFor++) {
  console.log(numeroFor);
}
